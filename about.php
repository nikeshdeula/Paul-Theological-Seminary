        <h4>Vision</h4>
        <p>The vision of Paul Theological Seminary is to develop, discover, equip and send effective spirit filled
          leaders for church planting movements who will impact Nepali society as well as world for Christ.</p>

        <h4>Mission</h4>
        <p>The mission of Paul Theological Seminary is to train village pastors and leaders as well as church members
          who are lacking for Biblical knowledge and willing to do full time ministry, and equip themselves for the word
          of God. </p>

        <h4>Brief History of PTS</h4>
        <p>This is the college established by one of the Korean Missionary called Dr. Rev. Joong Hyun Cho (Jeevan Cho).
          He came to Nepal on 30 March, 2000 AD and this college was founded in 2009 AD. Paul Theological Seminary (PTS)
          was established in Himalayan country Nepal. At the time of establishment there were only 14 students and
          classes were run in a small rented house. After their graduation all of them have gone to mission field and
          have done very good ministry. They have been involved in 40 different types of ministries such as church
          planting, teaching, training and leadership development. After staying four years at the same building our
          college have been shifted to four story big building with a 6,700 square feet of land. There is enough space
          of land, for boys and girls hostel, class rooms, dining hall as well as play grounds. So far as 118 students
          have been graduated from this seminary. We have been planning to shift in our own land and building after the
          completion of construction. There are 34 regular students studying in the seminary at present.</p>

        <h4>Dynamic fellowship</h4>
        <p>The picnic programs provide mutual understanding with the students, staff and professors. Faulty plays an
          important role in advising students regarding their personal, marital and other spiritual challenges so as to
          foster spiritual growth. </p>

        <h4>Program Offered</h4>
        <p>1.&nbsp; Bachelor of Theology (B.Th.)
          PTS offers Bachelor of Theology program. This is three years residential program for students who have
          completed higher secondary (12th standard) education. This program is designed to prepare pastors and ministry
          leaders for the churches in Nepal and around the Nepali communities in the world.<br>
          2.&nbsp; Certificate in Theology (C. Th.) 
          PTS offers Certificate in Theology program. This is two years residential program for students who have
          completed school level education (8-10th standard). This program is designed to prepare village pastors and
          leaders for ministry, who can return to their respective homes after their graduation.
        </p>

        <h4>Educational Tour</h4>
        <p>Every year PTS takes the students to educational tour for 2-3 days. It is great help for them visiting new
          places, doing recreational activities and sightseeing. For many years, we are taking them to Pokhara and
          surrounding areas, where they can enjoy and relax for few days as a part of their studies.</p>

        <h4>Faculty</h4>
        <p>Most of our faculties are qualitied, well trained and they have more than enough experience in ministry as well as teaching fields. Most of them have gone to abroad and got the sound theological trainings. Some of them have got theological trainings inside Nepal. Most of our faculties are pastors, so they are teaching our students practical things and practical subjects.</p>

          <h4>Teaching Subjects</h4>
        <p>PTS teach the following subjects with 3 years of period, like: English, Music, Greek, Hebrew, New Testaments, Old Testaments, Theology, Church History, Mission, Biblical books, Religions, Christian Education, Discipleship, Christian Ethics, Leadership, Preaching, Pastoral Care and Counselling, Cults, Psychology, Pastoral Ministry, Apologetics, Christian Marriage and Family, Integral Mission, Systematic Bible study, Income generations trainings etc.  </p>

